const translations: Record<Locale, TranslationMap> = { es, en };

export function t(key: string, locale: Locale, replacements?: Record<string, string | number>): string {
  let text = translations[locale]?.[key] ?? translations.es[key] ?? key;
  if (replacements) {
    for (const [k, v] of Object.entries(replacements)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}

export function keyCount(): number {
  return Object.keys(es).length;
}

