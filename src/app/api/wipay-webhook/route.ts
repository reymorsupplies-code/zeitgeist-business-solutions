import { NextRequest, NextResponse } from 'next/server';
import { pgQuery, pgQueryOne } from '@/lib/pg-query';

// ── WiPay Webhook Endpoint ──
// Receives POST notifications from WiPay when a payment status changes.
// This is a global endpoint (not tenant-scoped) — tenant is resolved from
// the transaction record via rentPaymentId.
//
// Flow:
//   1. Verify webhook signature using WiPay API key
//   2. Look up the WiPayTransaction record
//   3. Update transaction status
//   4. Update RentPayment status (mark as paid)
//   5. Send notification to landlord
//   6. Send receipt email to renter

// Ensure WiPayTransaction table exists (idempotent)
async function ensureWiPayTable() {
  await pgQuery(`
    CREATE TABLE IF NOT EXISTS "WiPayTransaction" (
      id              TEXT PRIMARY KEY DEFAULT gen_random_uuid()::text,
      "tenantId"      TEXT NOT NULL,
      "rentPaymentId" TEXT,
      "renterId"      TEXT,
      "paymentMethod" TEXT NOT NULL DEFAULT 'wipay',
      status          TEXT NOT NULL DEFAULT 'initiated',
      amount          NUMERIC(14,2) NOT NULL DEFAULT 0,
      currency        TEXT NOT NULL DEFAULT 'TTD',
      "processingFee" NUMERIC(14,2) NOT NULL DEFAULT 0,
      "wipayOrderId"      TEXT,
      "wipayTransactionId" TEXT,
      "wipayFee"          NUMERIC(14,2) NOT NULL DEFAULT 0,
      "wipayCurrency"     TEXT,
      "cardType"          TEXT,
      "cardLast4"         TEXT,
      "customerName"  TEXT,
      "customerEmail" TEXT,
      "customerPhone" TEXT,
      "cashReference" TEXT,
      "cashNotes"     TEXT,
      "refundAmount"    NUMERIC(14,2) NOT NULL DEFAULT 0,
      "refundReason"    TEXT,
      "refundedAt"      TIMESTAMPTZ,
      "ipAddress"       TEXT,
      "initiatedAt"   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      "completedAt"   TIMESTAMPTZ,
      "createdAt"     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      "updatedAt"     TIMESTAMPTZ NOT NULL DEFAULT NOW()
    )
  `);
  await pgQuery(`CREATE INDEX IF NOT EXISTS "idx_WiPayTransaction_tenantId" ON "WiPayTransaction"("tenantId")`);
  await pgQuery(`CREATE INDEX IF NOT EXISTS "idx_WiPayTransaction_rentPaymentId" ON "WiPayTransaction"("rentPaymentId")`);
  await pgQuery(`CREATE INDEX IF NOT EXISTS "idx_WiPayTransaction_wipayTransactionId" ON "WiPayTransaction"("wipayTransactionId")`);
  await pgQuery(`CREATE INDEX IF NOT EXISTS "idx_WiPayTransaction_tenant_status" ON "WiPayTransaction"("tenantId", status)`);
}

/**
 * Send a notification to the landlord about a payment event.
 */
async function notifyLandlord(tenantId: string, title: string, message: string, metadata: any) {
  try {
    const { db } = await import('@/lib/db');
    await db.notification.create({
      data: {
        tenantId,
        title,
        message,
        type: 'payment_received',
        category: 'payment',
        metadata: JSON.stringify(metadata),
      },
    });
  } catch (error) {
    console.error('[WiPay Webhook] Failed to create landlord notification:', error);
  }
}

